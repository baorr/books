const abs = Math.abs;

const getBandWidth = function(min, max, groupNumber){
	return abs(max - min) / groupNumber;
};

const category = function(arr, groupNumber){
	let lens = arr.length;
	let min = arr[0];
	let max = arr[lens - 1];
	let step = min;

	let bandwidth = getBandWidth(min, max, groupNumber);
	let ranges = [];

	for(let i = 0;i < groupNumber;i++){
		step += bandwidth;
		ranges.push(step);
	}

	let results = [];
	let flag = 0;
	let index = 0;
	let range = ranges[index];

	for(let i = 0;i < lens;i++){
		let item = arr[i];
		if(item > range){
			while(item > range){
				range = ranges[++index];
			}
			flag++;
		};

		if(!results[flag]){
			results[flag] = {
				items: [],
				min: item,
				max: range
			};
		};
		results[flag].items.push(item);
	}

	if(results[flag].max === undefined){
		results[flag].max = results[flag].min + bandwidth;
	}
	return results;
};


